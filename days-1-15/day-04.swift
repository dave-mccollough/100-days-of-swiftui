// Type annotations

// Swift uses type inference, but you can override that inference

// String
let mountainName: String = "Hallet Peak"

// Int
let elevation: Int = 12720

// Double
let entryFee: Double = 50.00

// Boolean
let frontRangePeak: Bool = true

// Array
// holds lots of different values, all in the order you add them. This must be specialized, such as [String]:
let mountains: [String] = ["Lomgs Peak", "Pikes Peak"]

// Dictionary
/// holds lots of different values, where you get to decide how data should be accessed. This must be specialized, such as [String: Int]:
let mountainRange: [String:String] = ["range" : "front"]

// Set
// holds lots of different values, but stores them in an order that’s optimized for checking what it contains. This must be specialized, such as Set<String>:

let sawatchPeaks: Set<String> = Set(["Holy Cross", "Massive", "Elbert"])







